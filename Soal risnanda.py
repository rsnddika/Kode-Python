hari = input("kode hari(angka <= 0 atau 0-6 atau >=6 atau) = ")

if (hari=="0"):
	print("Hari Minggu")#1
elif (hari=="1"):
	print("Hari Senin")#2
elif (hari=="2"):
	print("Hari Selasa")#3
elif (hari=="3"):
	print("Hari Rabu")#4
elif (hari=="4"):
	print("Hari Kamis")#5
elif (hari=="5"):
	print("Hari Jum'at")#6
elif (hari=="6"):
	print("Hari Sabtu")#7
else:
	print("Kode Salah")#8